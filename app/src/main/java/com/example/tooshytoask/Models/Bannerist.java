package com.example.tooshytoask.Models;

public class Bannerist {
    private String bannerId;
    private String bannerImg;
    private String bannerScreen;
    private String bannerBlogid;

    public String getBannerId() {
        return bannerId;
    }
    public void setBannerId(String bannerId) {
        this.bannerId = bannerId;
    }
    public String getBannerImg() {
        return bannerImg;
    }
    public void setBannerImg(String bannerImg) {
        this.bannerImg = bannerImg;
    }
    public String getBannerScreen() {
        return bannerScreen;
    }
    public void setBannerScreen(String bannerScreen) {
        this.bannerScreen = bannerScreen;
    }
    public String getBannerBlogid() {
        return bannerBlogid;
    }
    public void setBannerBlogid(String bannerBlogid) {
        this.bannerBlogid = bannerBlogid;
    }
}
