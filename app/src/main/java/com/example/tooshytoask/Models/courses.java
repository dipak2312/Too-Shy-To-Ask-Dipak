package com.example.tooshytoask.Models;

public class courses {
    private String bookmark_postid;
    private String bookmark_posttitle;
    private String bookmark_img;
    private String bookmark_type;
    private String bookmark_total_lesson;
    private String bookmark_timing;


    public String getBookmark_postid() {
        return bookmark_postid;
    }

    public void setBookmark_postid(String bookmark_postid) {
        this.bookmark_postid = bookmark_postid;
    }

    public String getBookmark_posttitle() {
        return bookmark_posttitle;
    }

    public void setBookmark_posttitle(String bookmark_posttitle) {
        this.bookmark_posttitle = bookmark_posttitle;
    }

    public String getBookmark_img() {
        return bookmark_img;
    }

    public void setBookmark_img(String bookmark_img) {
        this.bookmark_img = bookmark_img;
    }

    public String getBookmark_type() {
        return bookmark_type;
    }

    public void setBookmark_type(String bookmark_type) {
        this.bookmark_type = bookmark_type;
    }

    public String getBookmark_total_lesson() {
        return bookmark_total_lesson;
    }

    public void setBookmark_total_lesson(String bookmark_total_lesson) {
        this.bookmark_total_lesson = bookmark_total_lesson;
    }

    public String getBookmark_timing() {
        return bookmark_timing;
    }

    public void setBookmark_timing(String bookmark_timing) {
        this.bookmark_timing = bookmark_timing;
    }
}
