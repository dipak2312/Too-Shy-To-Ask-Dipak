package com.example.tooshytoask.Models.InsightScreen;

public class events {
    private String event_id;
    private String event_title;
    private String event_content;
    private String event_short;
    private String event_link;
    private String event_category;
    private String blog_img;
    private String blog_bookmarked;

    public String getEvent_id() {
        return event_id;
    }

    public void setEvent_id(String event_id) {
        this.event_id = event_id;
    }

    public String getEvent_title() {
        return event_title;
    }

    public void setEvent_title(String event_title) {
        this.event_title = event_title;
    }

    public String getEvent_content() {
        return event_content;
    }

    public void setEvent_content(String event_content) {
        this.event_content = event_content;
    }

    public String getEvent_short() {
        return event_short;
    }

    public void setEvent_short(String event_short) {
        this.event_short = event_short;
    }

    public String getEvent_link() {
        return event_link;
    }

    public void setEvent_link(String event_link) {
        this.event_link = event_link;
    }

    public String getEvent_category() {
        return event_category;
    }

    public void setEvent_category(String event_category) {
        this.event_category = event_category;
    }

    public String getBlog_img() {
        return blog_img;
    }

    public void setBlog_img(String blog_img) {
        this.blog_img = blog_img;
    }

    public String getBlog_bookmarked() {
        return blog_bookmarked;
    }

    public void setBlog_bookmarked(String blog_bookmarked) {
        this.blog_bookmarked = blog_bookmarked;
    }
}
