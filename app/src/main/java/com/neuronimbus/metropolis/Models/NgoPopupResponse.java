package com.neuronimbus.metropolis.Models;

public class NgoPopupResponse {
    private String code;
    private String msg;
    private String popup_msg;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public String getPopup_msg() {
        return popup_msg;
    }

    public void setPopup_msg(String popup_msg) {
        this.popup_msg = popup_msg;
    }
}
